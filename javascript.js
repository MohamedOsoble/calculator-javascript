
// addition function
function add(a, b){
    return a + b;
};

// subtract function
function subtract(a, b){
    return a - b;
};

// division function
function divide(a, b){
    return a/b;
};

// multiplication function
function multiply(a, b){
    return a*b;
};

console.log(add(5, 4));

console.log(subtract(20, 15));

console.log(multiply(15, 4));

console.log(divide(28, 7));